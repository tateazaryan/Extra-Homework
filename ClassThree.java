public class ClassThree {
    private double d1;
    private double d2;
    ClassThree(double d1, double d2){
        this.d1 = d1;
        this.d2 = d2;
    }
    public void setD1(double d1){
        this.d1 = d1;
    }
    public void setD2(double d2){
        this.d2 = d2;
    }
    public double getD2(){
        return this.d2;
    }
    public double getD1(){
        return this.d1;
    }
}